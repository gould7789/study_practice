for value in range(2):
    print(value)    # 0, 1

for value in range(3):
    print(value)    # 0, 1, 2

for value in range(1, 2):
    print(value)    # 1

for value in range(1, 3):
    print(value)    # 1, 2

for value in range(1, 11, 3):
    print(value)    # 1, 4, 7, 10

for value in range(10, 0, -3):
    print(value)    # 10, 7, 4, 1