# 사용자로부터 하나의 정수를 입력받아 해당 수의 구구단을 for문을 이용하여 출력하시오
# 단, 출력은 짝수 곱셈 결과만 출력되도록

"""
- 2단부터 9단까지 범위 중 하나의 정수 입력
- 입력값 유효성 검사: 2~9 사이가 아니면 오류 메시지 출력 후 종료
- 결과 중 곱하는 수(1~9)가 짝수인 경우만 출력
"""


# 사용자로부터 출력 할 단을 입력 받는다
input_number = int(input("출력할 단을 입력하세요 (2~9): "))

# 구구단 반복문 작성 '예외 처리 : 입력값이 2~9가 아니면 오류 메세지'
if 1 < input_number < 10:
    for i in range(2, 10, 2):
        print(f"{input_number} x {i} = {input_number * i}")
else:
    print("오류: 2단부터 9단까지만 입력 가능합니다.")