# 복합(compound) 연산자
# 이항 연산자
# 지정된 연산 후 대입 실시시


value = 1

value += 1          # value = value + 1

print(value)

value *= 3          # value = value * 3

print(value)

value /= 3          # value = value / 3

print(value)

value **= 10        # value = value ** 10

print(value)

value //= 1000      # value = value // 1000

print(value)